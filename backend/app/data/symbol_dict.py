def get_cn_name(symbol: str, market: Market) -> str | None:
    """根据市场和代码查找中文名称。"""
    if market == Market.US:
        return US_SHARES.get(symbol.upper())
    if market == Market.HK:
        # 港股代码统一为5位数字（去前导零后补到5位）
        s = symbol.lstrip("0").zfill(5) if symbol.isdigit() else symbol
        return HK_SHARES.get(s) or HK_SHARES.get(symbol)
    if market == Market.A:
        return A_SHARES.get(symbol)
    return None


def search_by_cn_name(query: str, market: Market | None = None) -> list[tuple[str, Market, str]]:
    """
    按中文名或代码模糊搜索，返回 (symbol, market, cn_name) 列表。
    """
    results: list[tuple[str, Market, str]] = []
    q = query.strip()

    def _match(sym: str, cn: str) -> bool:
        return q.lower() in sym.lower() or q in cn

    markets_to_search = [market] if market else [Market.US, Market.HK, Market.A]

    for m in markets_to_search:
        d: dict[str, str] = {}
        if m == Market.A:
            d = A_SHARES
        elif m == Market.HK:
            d = HK_SHARES
        elif m == Market.US:
            d = US_SHARES

        for sym, cn in d.items():
            if _match(sym, cn):
                results.append((sym, m, cn))

    return results[:50]


